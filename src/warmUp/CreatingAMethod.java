package warmUp;

public class CreatingAMethod {
	
	
	public void companyName(String company) {
		System.out.println("Company Name: " +company);
	}
	
	public void consoleName(String name) {
		System.out.println("Console Name: " +name);
	}
	
	public void type(String consoletype) {
		System.out.println("Console Type: " +consoletype);
	}
	
	public void color(String color) {
		System.out.println("Color: " +color);
	}
	
	public void date(String date) {
		System.out.println("Date Purchased: " +date);
	}

}
